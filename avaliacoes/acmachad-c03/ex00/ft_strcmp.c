#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	while (*s1 || *s2)
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		s1++;
		s2++;
	}
	return (0);
}

int main(void)
{
	char arr[] = "";
	char arr2[] = "";

	int x = ft_strcmp(arr, arr2);
	printf("%d", x);
}
