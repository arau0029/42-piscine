#include <stdio.h>
#include <unistd.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int	main(void)
{
	int	limit;
	char	src[] = "lais leal gomes vai passar na 42";
	char	dest[100];

	limit = 20;

	ft_strncpy(dest, src, limit);
	printf("%s", dest);
}

