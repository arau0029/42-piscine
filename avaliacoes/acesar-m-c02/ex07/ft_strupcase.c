char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] > 96 && str[i] < 123)
			str[i] -= 32;
		i++;
	}
	return (str);
}

#include <stdio.h>
int main(void)
{
	char transform[] = "Deu certo amigo!";

	printf("%s", ft_strupcase(transform));

	return (0);
}

