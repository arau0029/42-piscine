#include <stdio.h>
void	ft_swap(int *a, int *b)
{
	int	c;

	c = *a;
	*a = *b;
	*b = c;
}

int	main(void)
{
	int	a;
	int	b;

	a = 4;
	b = 2;
	ft_swap(&a, &b);
	printf("%i, %i", a, b);
	return (0);
}
