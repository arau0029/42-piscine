void	ft_swap(int *a, int *b)
{
	int	add;

	add = *a;
	*a = *b;
}

#include <stdio.h>

int	main(void)
{
	int	a;
	int	b;

	a = 40;
	b = 50;

	ft_swap(&a, &b);
	printf("%d\n", a);
	printf("%d\n", b);
}

